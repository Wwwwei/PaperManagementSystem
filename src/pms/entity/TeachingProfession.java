package pms.entity;

public class TeachingProfession {
	private int teachingProfession_id;
	private String teachingProfession_name;
	private String teachingProfession_info;
	
	public int getTeachingProfession_id() {
		return teachingProfession_id;
	}
	public void setTeachingProfession_id(int teachingProfession_id) {
		this.teachingProfession_id = teachingProfession_id;
	}
	public String getTeachingProfession_name() {
		return teachingProfession_name;
	}
	public void setTeachingProfession_name(String teachingProfession_name) {
		this.teachingProfession_name = teachingProfession_name;
	}
	public String getTeachingProfession_info() {
		return teachingProfession_info;
	}
	public void setTeachingProfession_info(String teachingProfession_info) {
		this.teachingProfession_info = teachingProfession_info;
	}
	

}
